class Book {
  final int id;
  final String titulo;
  final String subTitulo;
  final String isbn;
  final String autor;
  final String editora;
  final String local;
  final int ano;

  Book({
    required this.id,
    required this.titulo,
    required this.subTitulo,
    required this.isbn,
    required this.autor,
    required this.editora,
    required this.local,
    required this.ano,
  });
}
